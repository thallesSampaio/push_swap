#include "push_swap.h"

static void	rotate(t_stack *s)
{
	t_node	*first;

	if (!s || s -> size < 2)
		return ;
	first = s -> head;
	s -> head = first -> next;
	first -> next = NULL;
	s -> tail -> next = first;
	s -> tail = first;
}

void	ra(t_stack *a)
{
	rotate(a);
	write(1, "ra\n", 3);
}

void	rb(t_stack *b)
{
	rotate(b);
	write(1, "rb\n", 3);
}

void	rr(t_stack *a, t_stack *b)
{
	rotate(a);
	rotate(b);
	write(1, "rr\n", 3);
}
